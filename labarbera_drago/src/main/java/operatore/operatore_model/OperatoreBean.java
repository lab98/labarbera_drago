package operatore.operatore_model;

public class OperatoreBean {
	
	public OperatoreBean() {}
		
	private String cod_operatore;
	private String cod_asp;
	private String nome;
	private String cognome;
	private String email;
	private String ruolo;
	private String password;
	private String cod_fiscale;
	
	
	public String getCod_operatore() {
		return cod_operatore;
	}
	public void setCod_operatore(String cod_operatore) {
		this.cod_operatore = cod_operatore;
	}
	
	public String getCod_asp() {
		return cod_asp;
	}
	public void setCod_asp(String cod_asp) {
		this.cod_asp = cod_asp;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCognome() {
		return cognome;
	}
	public void setCognome(String cognome) {
		this.cognome = cognome;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getRuolo() {
		return ruolo;
	}
	public void setRuolo(String ruolo) {
		this.ruolo = ruolo;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password=password;
	}
	public String getCod_fiscale() {
		return cod_fiscale;
	}
	public void setCod_fiscale(String cod_fiscale) {
		this.cod_fiscale = cod_fiscale;
	}








}
